package hsegment.JObject.Swing.Text.xml;

public class AttributeKV {
    private String name;
    private String value;

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }

    public AttributeKV(String name, String value) {
        this.name = name;
        this.value = value;
    }
}
